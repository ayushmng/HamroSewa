public class UserInfo {

    private String mImageUrl;
    private String mCreator;


    public UserInfo(String imageUrl, String creator) {
        mImageUrl = imageUrl;
        mCreator = creator;

    }

    public String getImageUrl() {
        return mImageUrl;
    }

    public String getCreator() {
        return mCreator;
    }

 }





